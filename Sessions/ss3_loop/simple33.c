/*
 * Vong lap While
 * while (bieu thuc)
 * {
 *      khoi lenh
 * }
 */

/*
 * Giao vien yeu cau chep phat dong "Em xin loi co. Lan sau em se khong tai pham nua"
 */
#include <stdio.h>

int main() {
    int index = 0;
    while (index < 5) {
        printf("Em xin loi co. Lan sau em se khong tai pham nua\n");
        index++;
    }
    return 0;
}